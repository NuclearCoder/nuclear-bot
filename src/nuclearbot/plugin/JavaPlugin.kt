/**
 * Public API interface for a plugin wrapper with name, version, and built-in flag.<br></br>
 * <br></br>
 * NuclearBot (https://github.com/NuclearCoder/nuclear-bot/)<br></br>

 * @author NuclearCoder (contact on the GitHub repo)
 */
interface JavaPlugin : Plugin {

    /**
     * The plugin main class name.
     */
    val className: String

    /**
     * True if the plugin was loaded like a built-in plugin.
     */
    val isBuiltin: Boolean

    /**
     * The underlying Plugin instance wrapped by this object.
     */
    val handle: Plugin

}
